package programs.array;

public class SwapingTwoNumber
{
    public static void main(String[] args)
    {

        int a = 10  ;   //  a = 20

        int b = 20 ;  //  b = 10

       int temp = a ;  // temp = 10
        a = b ;
        b = temp ;


        System.out.println( a ); // a=20
        System.out.println( b ); // b=10

    }
}
